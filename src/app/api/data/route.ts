import { NextResponse } from 'next/server'
import { supabase } from '@/lib/supabase'

export async function GET() {
  const [
    { data: fy26 },
    { data: fy25 },
    { data: weekly },
    { data: prodMonthly },
    { data: prodWeekly },
    { data: prodSku },
    { data: issues },
    { data: calendar },
    { data: apCategories },
    { data: marketing },
    { data: largeTxns },
    { data: anomalies },
    { data: signals },
    { data: marketingDaily },
    { data: seoSnapshots },
    { data: syncMetadata },
    { data: prodRuns },
    { data: prodEfficiency },
    { data: marginSkus },
    { data: inventorySnap },
    { data: plMonthly },
    { data: shopifyCohorts },
    { data: xeroExec },
    { data: prodSchedule },
    { data: xeroArInvoices },
    { data: costingCogs },
    { data: costingIngredients },
    { data: costingPackaging },
    { data: costingOverheads },
    { data: dailyMetrics },
    { data: inventoryBatches },
  ] = await Promise.all([
    supabase.from('revenue_monthly').select('*').eq('fiscal_year', 'fy26').order('sort_order'),
    supabase.from('revenue_monthly').select('*').eq('fiscal_year', 'fy25').order('sort_order'),
    supabase.from('revenue_weekly').select('*').order('sort_order'),
    supabase.from('production_monthly').select('*').order('sort_order'),
    supabase.from('production_weekly').select('*').order('sort_order'),
    supabase.from('production_sku').select('*').order('sort_order'),
    supabase.from('issues').select('*').order('date'),
    supabase.from('calendar_events').select('*').order('sort_order'),
    supabase.from('ap_categories').select('*').order('sort_order').limit(20),
    supabase.from('marketing_monthly').select('*').order('sort_order'),
    supabase.from('large_transactions').select('*').order('sort_order'),
    supabase.from('anomalies').select('*'),
    supabase.from('signals').select('*').order('id', { ascending: false }),
    supabase.from('marketing_daily').select('*').order('date', { ascending: false }),
    supabase.from('seo_snapshots').select('*').order('date', { ascending: false }),
    supabase.from('sync_metadata').select('*'),
    supabase.from('prod_runs').select('*').order('run_date', { ascending: false }),
    supabase.from('prod_efficiency').select('*').order('sort_order'),
    supabase.from('margin_skus').select('*'),
    supabase.from('inventory_snapshot').select('*').order('snapshot_date', { ascending: false }).limit(20),
    supabase.from('pl_monthly').select('*').order('sort_order'),
    supabase.from('shopify_cohorts').select('*').order('cohort_month'),
    supabase.from('xero_exec_summary').select('*').order('sort_order'),
    supabase.from('prod_schedule').select('*').order('run_date'),
    supabase.from('xero_ar_invoices').select('*').order('days_overdue', { ascending: false }),
    supabase.from('costing_cogs').select('*').order('sku_name'),
    supabase.from('costing_ingredients').select('*').order('sku_name'),
    supabase.from('costing_packaging').select('*').order('sku_name'),
    supabase.from('costing_overheads').select('*').order('sku_name'),
    supabase.from('daily_metrics').select('*').order('metric_date', { ascending: false }).limit(365),
    supabase.from('inventory_batches').select('*').order('product_type'),
  ])

  return NextResponse.json({
    fy26: fy26 ?? [],
    fy25: fy25 ?? [],
    weekly: weekly ?? [],
    prodMonthly: prodMonthly ?? [],
    prodWeekly: prodWeekly ?? [],
    prodSku: prodSku ?? [],
    issues: issues ?? [],
    calendar: calendar ?? [],
    apCategories: apCategories ?? [],
    marketing: marketing ?? [],
    largeTxns: largeTxns ?? [],
    anomalies: anomalies ?? [],
    signals: signals ?? [],
    marketingDaily: marketingDaily ?? [],
    seoSnapshots: seoSnapshots ?? [],
    syncMetadata: syncMetadata ?? [],
    prodRuns: prodRuns ?? [],
    prodEfficiency: prodEfficiency ?? [],
    marginSkus: marginSkus ?? [],
    inventorySnapshot: inventorySnap ?? [],
    plMonthly: plMonthly ?? [],
    shopifyCohorts: shopifyCohorts ?? [],
    xeroExecSummary: xeroExec ?? [],
    prodSchedule: prodSchedule ?? [],
    xeroArInvoices: xeroArInvoices ?? [],
    costingCogs: costingCogs ?? [],
    costingIngredients: costingIngredients ?? [],
    costingPackaging: costingPackaging ?? [],
    costingOverheads: costingOverheads ?? [],
    dailyMetrics: dailyMetrics ?? [],
    inventoryBatches: inventoryBatches ?? [],
  })
}
